package com.xms.oop.basic.day02;
/**
 * 关系运算符
 * @author Administrator
 *
 */

public class OperatorDemo02 {
	public static void main(String[] args) {
		int a=10;
		System.out.println(a>10);
		System.out.println(a>=10);
		System.out.println(a<10);
		System.out.println(a<=10);
		System.out.println(a==10);
		System.out.println(a!=10);
	}
}
